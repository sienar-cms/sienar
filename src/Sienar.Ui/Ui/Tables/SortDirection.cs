namespace Sienar.Ui;

/// <summary>
/// Represents a tri-state table sort direction
/// </summary>
public enum SortDirection
{
	/// <summary>
	/// Represents a table state with no sort direction applied
	/// </summary>
	None,

	/// <summary>
	/// Represents a table state with ascending sort direction applied
	/// </summary>
	Ascending,

	/// <summary>
	/// Represents a table state with descending sort direction applied
	/// </summary>
	Descending
}
